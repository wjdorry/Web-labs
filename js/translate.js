let currentLang = 'en';

function getTranslate(lang) {
  if (!i18Obj[lang]) {
    console.warn(`Language "${lang}" is not supported`);
    return;
  }
  
  currentLang = lang;
  const elements = document.querySelectorAll('[data-i18n]');
  
  elements.forEach(element => {
    const key = element.dataset.i18n;
    const translation = i18Obj[lang][key];
    
    if (translation !== undefined) {
      // Handle newline characters in translations
      element.textContent = translation.replace(/\\n/g, '\n');
      element.innerHTML = element.innerHTML.replace(/\n/g, '<br>');
    } else {
      console.warn(`Translation missing for key: "${key}"`);
    }
  });
  
  // Save language preference to localStorage
  localStorage.setItem('preferredLanguage', lang);
  
  // Update active language button
  updateActiveLanguageButton(lang);
}

function updateActiveLanguageButton(lang) {
  const langButtons = document.querySelectorAll('.lang-switcher__button');
  langButtons.forEach(btn => {
    if (btn.dataset.lang === lang) {
      btn.classList.add('is-active');
    } else {
      btn.classList.remove('is-active');
    }
  });
}

function initLanguage() {
  // Check for saved language preference
  const savedLang = localStorage.getItem('preferredLanguage') || 'en';
  
  // Get browser language as fallback
  const browserLang = navigator.language || navigator.userLanguage;
  const lang = savedLang || (browserLang.startsWith('ru') ? 'ru' : 'en');
  
  getTranslate(lang);
}

function switchLanguage(lang) {
  getTranslate(lang);
}

// Export functions for use in other modules
if (typeof module !== 'undefined' && module.exports) {
  module.exports = { getTranslate, initLanguage, switchLanguage, i18Obj };
}

